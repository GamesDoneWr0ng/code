class Node():
    def __init__(self, parent, position):
        self.parent = parent
        self.position = position
        self.f = 0
        self.g = 0
        self.h = 0
        
    def __eq__(self, other):
        return self.position == other.position
    
def aStar(maze, start, end):
    startNode = Node(None, start)
    endNode = Node(Node, end)
    
    openList = []
    closedList = []
    
    openList.append(startNode)
    
    while len(openList) != 0:
        currentNode = openList[0]
        for i in openList:
            if i.f < currentNode.f:
                currentNode = i
        
        openList.remove(currentNode)
        closedList.append(currentNode)
        
        if currentNode == endNode:
            path = []
            current = currentNode
            while current is not None:
                path.append(current.position)
                current = current.parent
            return path[::-1]
        
        children = []
        for i in [(0, -1), (0, 1), (-1, 0), (1, 0)]:
            nodePosition = (currentNode.position[0] + i[0], currentNode.position[1] + i[1])
        
            if nodePosition[0] > (len(maze) - 1) or nodePosition[0] < 0 or nodePosition[1] > (len(maze[len(maze)-1]) -1) or nodePosition[1] < 0:
                continue
            
            if maze[nodePosition[0]][nodePosition[1]]:
                continue
            
            newNode = Node(currentNode, nodePosition)
            children.append(newNode)
            
        for child in children:
            flag = False
            for i in closedList:
                if child == i:
                    flag = True
                    break
                
            if flag:
                continue
            
            child.g = currentNode.g + 1
            child.h = abs(child.position[0] - endNode.position[0]) + abs(child.position[1] - endNode.position[1])
            child.f = child.g + child.h
            
            for i in openList:
                if child == i and child.g >= i.g:
                    openList.remove(i)
                    continue
            
            openList.append(child)
            
            
